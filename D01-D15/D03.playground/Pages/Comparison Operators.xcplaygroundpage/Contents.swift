import Foundation

let firstScore = 6
let secondScore = 4

// checks for whether firstScore is equal to secondScore
firstScore == secondScore

// checks for whether firstScore is not equal to secondScore
firstScore != secondScore

// checks for whether firstScore is smaller than secondScore
firstScore < secondScore

// checks for whether firstScore is greater than or equal to secondScore
firstScore >= secondScore

// each of these also work with strings, because strings have a natural alphabetical order:
"Taylor" <= "Swift"

